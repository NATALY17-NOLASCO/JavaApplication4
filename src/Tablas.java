
public class Tablas {
    public static void main (String [] args){
        int a, b;
        for (a=1; a<=10; a++){
            System.out.println();
            System.out.println("Tabla del " + a + ":");
            for (b=1; b<=10; b++){
                System.out.println(a + "x" + b + "=" + (a*b));
                
  
            }
        }
    }
    
}
